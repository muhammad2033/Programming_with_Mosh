# comparison operator we have
            # <,>,==,<=,>=
price = 4
if price > 8 :                  #greater than 8
    print("price is okey!")
else:
    print("exorbitant!")    

if price < 8 :         # less than 8
    print("price is okey!")
else:
    print("exorbitant!")    

if price <= 8 :          #less than and equal to 8
    print("price is okey!")
else:
    print("exorbitant!")    

if price >= 8 :                 #greater than and equal to 8 
    print("price is okey!")
else:
    print("exorbitant!")    

if price == 8 :                  #equal to 8
    print("price is okey!")
else:
    print("exorbitant!")        


